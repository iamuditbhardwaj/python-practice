# Tuples

# A tuple is a collection of items just like a list. It can have items of different data type.
a = (1, 3, "Henry", 33.4, True)
print(a, type(a))
b = () # Empty tuple
print(b, type(b))
c = (2,) # Tuple with a single element
print(c, type(c))

# It is immutable.
# One cannot change an exsiting tuple in a program.
