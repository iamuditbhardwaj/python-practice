# Variables in Python
name = "James" #String variable
age = 18  #Integer variable
height = 5.6  #Float variable
is_student = True  #Boolean variable

# printing the person's details
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Rules for naming variables in Python:
# 1. Variable names can only contain letters, numbers, and underscores.
# 2. Variable names can start with a letter or an underscore.
# 3. Variable names cannot start with a number.
# 4. Variable names cannot contain spaces or special characters.
# 5. Python variable names are case-sensitive.
# Example: age and Age are different variables
