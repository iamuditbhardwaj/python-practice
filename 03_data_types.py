# Data Types in Python

# String
name = "Alice"  
print(type(name))  # <class 'str'>

# Integer
age = 18  
print(type(age))  # <class 'int'>

# Float
height = 5.6  
print(type(height))  # <class 'float'>

# Boolean
is_student = True  
print(type(is_student))  # <class 'bool'>

# NoneType
e = None  
print(type(e))  # <class 'NoneType'>

